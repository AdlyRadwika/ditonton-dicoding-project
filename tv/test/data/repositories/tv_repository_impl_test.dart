    genreIds: [18, 80],
    id: 31586,
    overview:
        'After years of blood, sweat and tears, a woman of humble origin ends up becoming a drug trafficking legend, with all that that means...',
    posterPath: '/uBTlJDdPpRxYTfUnKw4wbuIGSEK.jpg',
    name: 'La Reina del Sur',
    voteAverage: 7.773,
  );

